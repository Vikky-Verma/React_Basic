// ways to create react project 
// 1. using create-react-app
// 2. using vite
// 3. using webpack and babel

// using create-react-app
// 1. open terminal and run the command 
// npx create-react-app my-app
// 2. navigate to the project directory
// cd my-app
// 3. start the development server
// npm start or npm run start
// npm build  or npm run build to create a production build of the app it includes static files like html css and javascript and media files that can be deployed to a web server or hosting service.



// using vite
// 1. open terminal and run the command
// npm create vite@latest my-app --template react
// 2. navigate to the project directory
// cd my-app
// 3. install dependencies
// npm install
// 4. start the development server
// npm run dev
// npm build or npm run build to create a production build of the app it includes static files like html css and javascript and media files that can be deployed to a web server or hosting service.

